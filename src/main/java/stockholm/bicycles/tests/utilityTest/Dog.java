package stockholm.bicycles.tests.utilityTest;

public class Dog {
	private Double size;
	private String name;

	public Dog(double size, String name) {
		super();
		this.size = size;
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setSize(Double size) {
		this.size = size;
	}



	public Double getSize() {
		return size;
	}

	public void setSize(double size) {
		this.size = size;
	}

}
