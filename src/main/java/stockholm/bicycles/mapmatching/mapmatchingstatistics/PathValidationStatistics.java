package stockholm.bicycles.mapmatching.mapmatchingstatistics;

public class PathValidationStatistics {
	private double percentageLengthMatched=-1;
	private double percentageLengthIncorrectMatched=-1;

	public double getPercentageLengthMatched() {
		return percentageLengthMatched;
	}

	public void setPercentageLengthMatched(double percentageLengthMatched) {
		this.percentageLengthMatched = percentageLengthMatched;
	}

	public double getPercentageLengthIncorrectMatched() {
		return percentageLengthIncorrectMatched;
	}

	public void setPercentageLengthIncorrectMatched(double percentageLengthIncorrectMatched) {
		this.percentageLengthIncorrectMatched = percentageLengthIncorrectMatched;
	}
	
	
	

}
